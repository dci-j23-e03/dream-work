package com.dreamwork.model.job;

/**
 * Enum representing the seniority levels for job positions.
 */
public enum Seniority {

  /**
   * Represents an internship position
   */
  INTERN,

  /**
   * Represents a junior-level position
   */
  JUNIOR,

  /**
   * Represents a mid-level position
   */
  MID,

  /**
   * Represents a senior-level position
   */
  SENIOR,

  /**
   * Represents a lead-level position
   */
  LEAD
}
